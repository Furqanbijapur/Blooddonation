          </ol><center><div><h2>Beat the myth</h2></div></center>
        <p>Donating blood is safe and simple.
            It takes approximately 10-15 minutes to complete the blood donation process. Any healthy adult between 18 years and 60 years of age can donate blood. This is what you can expect when you are ready to donate blood:</p>
            <ul>
<li>You walk into a reputed and safe blood donation centre or a mobile camp organized by a reputed institution.</li>
<li>A few questions will be asked to determine your health status (general questions on health, donation history etc). Usually you will be asked to fill out a short form.</li>
<li>Then a quick physical check will be done to check temperature, blood pressure, pulse and hemoglobin content in blood to ensure you are a healthy donor.</li>
<li>If found fit to donate, then you will be asked to lie down on a resting chair or a bed. Your arm will be thoroughly cleaned. Then using sterile
    equipments blood will be collected in a special plastic bag.</li>
<li>Approximately 350 ml of blood will be collected in one donation. Those who weigh more than 60 Kg can donate 450 ml of blood.</li>
<li>Then you must rest and relax for a few minutes with a light snack and something refreshing to drink. Some snacks and juice will be provided.</li>
<li>Blood will be separated into components within eight hours of donation</li>
<li>The blood will then be taken to the laboratory for testing.</li>
<li>Once found safe, it will be kept in special storage and released when required.</li>
<li>The blood is now ready to be taken to the hospital, to save lives.</li></ul>
        <center><div><h2>BLOOD GROUPS</h2></div></center>
    <p>Blood type is determined by which antibodies and antigens the person's blood produces. An individual has two types of blood groups namely ABO-grouping and Rh-grouping.
        Rh is called as the Rhesus factor that has come to us from Rhesus monkeys.</p>
    <p>Most humans are in the ABO blood group. The ABO group has four categories namely
        1) A group 2) B group 3) O group and 4) AB group</p>
    <p>In the Rh- group, either the individual is said to be Rh- Negative or Rh- Positive.</p>
<p>Thus blood group of any human being will mainly fall in any one of the following groups.
</p><ul><li>A positive or A negative</li>
<li>B positive or B negative</li>
<li>O positive or O negative</li>
<li>AB positive or AB negative.</li></ul>

<center><div><h2>DO donate blood, only if you satisfy all of the following conditions</h2></div></center>
<p>&#10004; You are between age group of 18-60 years.</p>
<p>&#10004; Your weight is 45 kgs or more.</p>
<p>&#10004; Your hemoglobin is 12.5 gm% minimum.</p>
<p>&#10004; Your last blood donation was 3 or more months earlier.</p>
<p>&#10004; You are healthy and have not suffered from malaria,
    typhoid or other transmissible disease in the recent past.</p>


<center><div><h2>DO NOT donate blood, if you have any of the following conditions</h2></div></center>
<p>&#10060; Cold / fever in the past 1 week.</p>
<p>&#10060; Under treatment with antibiotics or any other medication.</p>
<p>&#10060; Your hemoglobin is 12.5 gm% minimum.</p>
<p>&#10060; Cardiac problems, hypertension, epilepsy, diabetes
    (on insulin therapy), history of cancer, chronic kidney
     or liver disease, bleeding tendencies, venereal disease etc.
</p>
<p>&#10060; Vaccination in the last 24 hours.</p>
<p>&#10060; Had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year.</p>
<p>&#10060; Had fainting attacks during last donation.
</p>
<p>&#10060; Have regularly received treatment with blood products.
</p>
<p>&#10060; Shared a needle to inject drugs/ have history of drug addiction.

</p>
<p>&#10060; Had sexual relations with different partners or with a high risk individual.
</p>
<p>&#10060; Been tested positive for antibodies to HIV.</p>

